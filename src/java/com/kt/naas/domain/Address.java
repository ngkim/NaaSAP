package com.kt.naas.domain;

public class Address {

	private int num;
	private String addr;
	private String detailaddr;
	private String contactname;
	private String contactnum;
	private String nmsname;
	private String nmscontractname;
	private String nmscontractnum;
	private String custname;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getDetailAddr() {
		return detailaddr;
	}

	public void setDetailAddr(String detailAddr) {
		this.detailaddr = detailAddr;
	}

	public String getContactName() {
		return contactname;
	}

	public void setContactName(String contactName) {
		this.contactname = contactName;
	}

	public String getContactNum() {
		return contactnum;
	}

	public void setContactNum(String contactNum) {
		this.contactnum = contactNum;
	}

	public String getNmsName() {
		return nmsname;
	}

	public void setNmsName(String nmsName) {
		this.nmsname = nmsName;
	}

	public String getNmsContractName() {
		return nmscontractname;
	}

	public void setNmsContractName(String nmsContractName) {
		this.nmscontractname = nmsContractName;
	}

	public String getCustName() {
		return custname;
	}

	public void setCustName(String custName) {
		this.custname = custName;
	}

	public String getNmscontractnum() {
		return nmscontractnum;
	}

	public void setNmscontractnum(String nmscontractnum) {
		this.nmscontractnum = nmscontractnum;
	}

}
